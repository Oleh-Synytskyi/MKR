class Pathfinder:
    # Setup
    def __init__(self, matrix):
        self.matrix = matrix
        self.grid = Grid(matrix=matrix)
        self.select_surf = pygame.image.load('Pictures 3x3/selection.png').convert_alpha()

        # pathfinding
        self.path = []

    def draw_active_cell(self):
        mouse_pos = pygame.mouse.get_pos()
        row = mouse_pos[1] // 32
        col = mouse_pos[0] // 32
        current_cell_value = self.matrix[row][col]
        if current_cell_value == 1:
            rect = pygame.Rect((col * 32, row * 32), (32, 32))
            screen.blit(self.select_surf, rect)

    def create_path(self):
# start
       start_x, start_y = [1, 1]
       start = self.grid.node(start_x, start_y)

# end
       mouse_position = pygame.mouse.get_pos()
       end_x, end_y = mouse_position[0] // 32, mouse_position[1] // 32
       end = self.grid.node(end_x, end_y)
# path
       finder = asf(diagonal_movement=DM.always)
       self.path,_ = finder.find_path(start, end, self.grid)
       self.grid.cleanup()

    def draw_path(self):
        if self.path:
            points = []
            for point in self.path:
                x = (point[0] * 32) + 16
                y = (point[1] * 32) + 16
                points.append((x, y))
                pygame.draw.circle(screen, '#4a4a4a', (x,y), 20)
            pygame.draw.lines(screen, '#4a4a4a', False, points, 5)

    def update(self):
        self.draw_active_cell()
        self.draw_path()

