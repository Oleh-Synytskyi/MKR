class Pathfinder:
    # Setup
    def __init__(self, matrix) -> None:

        self.matrix = matrix
        self.grid = Grid(matrix=matrix)
        self.select_surf = pygame.image.load('Pictures 3x3/selection.png').convert_alpha()

        # pathfinding
        self.path = []

        # Roomba
        self.roomba = pygame.sprite.GroupSingle(Roomba())

    def draw_active_cell(self) -> None:
        mouse_pos = pygame.mouse.get_pos()
        row = mouse_pos[1] // 32
        col = mouse_pos[0] // 32
        current_cell_value = self.matrix[row][col]
        if current_cell_value == 1:
            rect = pygame.Rect((col * 32, row * 32), (32, 32))
            screen.blit(self.select_surf, rect)

    def create_path(self) -> None:
# start
        start_x, start_y = self.roomba.sprite.get_coord()
        start = self.grid.node(start_x, start_y)

# end
        mouse_pos = pygame.mouse.get_pos()
        end_x, end_y = mouse_pos[0] // 32, mouse_pos[1] // 32
        end = self.grid.node(end_x, end_y)
# path
        finder = asf(diagonal_movement=DM.always)
        self.path,_ = finder.find_path(start, end, self.grid)
        self.grid.cleanup()
        self.roomba.sprite.set_path(self.path)

    def draw_path(self) -> None:
        if len(self.path) > 2:
            points = []
            for index,point in enumerate(self.path):
                x = point.x * 32 + 16 / 2
                y = point.y * 32 + 16 / 2
                points.append((x, y))
                pygame.draw.circle(screen, '#0385ff', (x, y), 10)
            pygame.draw.lines(screen, '#0385ff', False, points, 5)

    def update(self) -> None:
        self.draw_active_cell()
        self.draw_path()

        # roomba update and drawing
        self.roomba.update()
        self.roomba.draw(screen)

class Roomba(pygame.sprite.Sprite):
    def __init__(self) -> None:

        # basic
        super().__init__()
        self.image = pygame.image.load('Pictures 3x3/roomba.png').convert_alpha()
        self.rect = self.image.get_rect(center = (60, 60))

        # movement
        self.pos = self.rect.center
        self.speed = 0.5
        self.direction = pygame.math.Vector2(0,0)

        # path
        self.path = []
        self.collision_rects = []

    def get_coord(self) -> None:
        col = self.rect.centerx // 32
        row = self.rect.centery // 32
        return (col, row)

    def set_path(self, path) -> None:
        self.path = path
        self.create_collision_rects()
        self.get_direction()

    def create_collision_rects(self) -> None:
        if len(self.path) > 2:
            self.collision_rects = []
            for index,point in enumerate(self.path):
               x = point.x * 32 + 16 / 2
               y = point.y * 32 + 16 / 2
               rect = pygame.Rect((x - 2, y - 2), (4, 4))
               self.collision_rects.append(rect)

    def get_direction(self) -> None:
        if self.collision_rects:
            start = pygame.math.Vector2(self.pos)
            end = pygame.math.Vector2(self.collision_rects[0].center)
            self.direction = (end - start).normalize()
        else:
            self.direction = pygame.math.Vector2(0,0)
            self.path = []

    def check_collisions(self) -> None:
        if self.collision_rects:
            for rect in self.collision_rects:
                if rect.collidepoint(self.pos):
                    del self.collision_rects[0]
                    self.get_direction()

    def update(self) -> None:
        self.pos += self.direction * self.speed
        self.check_collisions()
        self.rect.center = self.pos

